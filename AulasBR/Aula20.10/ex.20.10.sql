-- CONSULTAS (JUNÇÕES - JOIN)

SELECT venda.DUPLIC, cliente.NOME FROM cliente, venda WHERE cliente.CODCLI = venda.CODCLI;

-- COM INNER JOIN

SELECT venda.DUPLIC, cliente.NOME FROM cliente INNER JOIN venda ON cliente.CODCLI = venda.CODCLI;

-- COM ALIAS

SELECT ven.DUPLIC, cli.NOME FROM cliente cli INNER JOIN venda ven ON cli.CODCLI = ven.CODCLI;

-- Com Order by
SELECT ven.DUPLIC, cli.NOME FROM cliente cli INNER JOIN venda ven ON cli.CODCLI = ven.CODCLI ORDER BY cli.NOME;

-- COM LEFT JOIN
SELECT ven.DUPLIC, cli.NOME
FROM cliente cli LEFT JOIN venda ven ON cli.CODCLI = ven.CODCLI
ORDER BY cli.NOME;

-- RIGHT JOIN
SELECT ven.DUPLIC, cli.NOME, cli.CIDADE
FROM cliente cli RIGHT JOIN venda ven ON cli.CODCLI = ven.CODCLI
ORDER BY cli.NOME;

-- QUANTIDADE
SELECT cliente.NOME, COUNT(*) AS QTDE
FROM cliente INNER JOIN venda ON cliente.CODCLI = venda.CODCLI
